"""
Funciones lambda: Nos sirve para crear funciones anonimas en una linea de codigo
las cuales son sobre la demanda de la funcion misma

Declaracion:
nombre_de_la_funcion = lambda parametros : sentencia

deben ser lo mas simple posible
"""

suma_lambda = lambda pri, sec : pri + sec

res = suma_lambda(10,23)
print(res)
